package hackerank;

import java.util.Scanner;

public class JavaRegex {


    public static void main(String[] args) {
        //        TC
        //        000.12.12.034 T
        //        121.234.12.12 T
        //        23.45.12.56 T
        //        00.12.123.123123.123 F
        //        122.23 F
        //        Hello.IP F
        //        259.259.259.259 F
        //        266.266.266.266 F
        //        255.255.255.255 T
        //        555.555.555.555 F
        //        666.666.666.666 F
        //        249.249.249.249 T
        //        249.249.249.256 F
        Scanner in = new Scanner(System.in);
        String pattern = "(([0-2][0-4][0-9]\\.)|([0-2][5][0-5]\\.)|([0-9][0-9]\\.)|([0-9]\\.))(([0-2][0-4][0-9]\\.)|([0-2][5][0-5]\\.)|([0-9][0-9]\\.)|([0-9]\\.))(([0-2][0-4][0-9]\\.)|([0-2][5][0-5]\\.)|([0-9][0-9]\\.)|([0-9]\\.))(([0-2][0-4][0-9])|([0-2][5][0-5])|([0-9][0-9])|([0-9]))$";
        while(in.hasNext()){
            String IP = in.next();
            System.out.println(IP.matches(pattern));
        }
    }
}

